void PhaseExecute(uint8_t phase, uint32_t delay, uint8_t rev)
{
	const uint8_t* phase_l = phases[phase];

	if (!rev) {
		HAL_GPIO_WritePin(GPIOB, ENV, phase_l[0]);
		HAL_GPIO_WritePin(GPIOA, INV, phase_l[1]);
		HAL_GPIO_WritePin(GPIOB, ENW, phase_l[2]);
		HAL_GPIO_WritePin(GPIOA, INW, phase_l[3]);
		HAL_GPIO_WritePin(GPIOB, ENU, phase_l[4]);
		HAL_GPIO_WritePin(GPIOA, INU, phase_l[5]);
	} else {
		HAL_GPIO_WritePin(GPIOB, ENV, phase_l[5]);
		HAL_GPIO_WritePin(GPIOA, INV, phase_l[4]);
		HAL_GPIO_WritePin(GPIOB, ENW, phase_l[3]);
		HAL_GPIO_WritePin(GPIOA, INW, phase_l[2]);
		HAL_GPIO_WritePin(GPIOB, ENU, phase_l[1]);
		HAL_GPIO_WritePin(GPIOA, INU, phase_l[0]);
	}

	HAL_Delay(delay);
}

